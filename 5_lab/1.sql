SELECT A1.IDSTUDENT, A1.NOTE
FROM PROGRESS AS A1
WHERE A1.NOTE >= ALL(SELECT A2.NOTE 
FROM PROGRESS AS A2
WHERE A1.IDSTUDENT !=A2.IDSTUDENT);
