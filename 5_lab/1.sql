SELECT DISTINCT S1.IDSTUDENT, S1.BDAY
FROM STUDENT AS S1
WHERE S1.BDAY = ANY (SELECT S2.BDAY FROM STUDENT AS S2
WHERE S1.IDSTUDENT!=S2.IDSTUDENT)
ORDER BY BDAY;
